package com.sarveshapps.dungeonescape;

/**
 * A public enum that hold the types of entities in the game
 *
 *  @author Sarvesh Somasundaram
 *  @version 1.0
 *
 *  @see <a href=" https://github.com/Sarvesh1234567/FinalProjDungeonEscape ">Github Link to Project Repo</a>
 *  @see <a href=" https://github.com/AlmasB/FXGL ">JavaFX Game Library</a>
 *  @see <a href=" https://opengameart.org/ ">Textures, animations and backgrounds</a>
 *
 */

public enum DungeonType {
    /**
     * Used to set type for a player entity
     */
    PLAYER,

    /**
     * Used to set type for an enemy entity
     */
    ENEMY,

    /**
     * Used to set type for the floor entity
     */
    FLOOR,

    /**
     * Used to set type for the wall entity
     */
    WALL,

    /**
     * Used to set type for the magic entity
     */
    MAGIC,

    /**
     * Used to set type for the exit entity
     */
    EXIT
}
